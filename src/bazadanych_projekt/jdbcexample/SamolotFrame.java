    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       SamolotDao pdao = new JdbcSamolotDao();
       Samolot samolot = new Samolot();
       samolot.setId_linii_lotniczej(Integer.parseInt(this.jTextField1.getText()));
       samolot.setProducent(this.jTextField2.getText());
       samolot.setModel(this.jTextField3.getText());
       samolot.setIlosc_miejsc(Integer.parseInt(this.jTextField4.getText()));
       samolot.setRok_produkcji(Date.valueOf((this.jTextField5.getText())));
       samolot.setRodzaj(this.jTextField6.getText());
       pdao.save(samolot);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       SamolotDao pdao = new JdbcSamolotDao(); 
        List<Samolot> list = pdao.findAll();
       DefaultListModel listModel = new DefaultListModel();
       for(Samolot x : list)
       {
           listModel.addElement(x.toString());
       }
       this.jList1.setModel(listModel);
    }//GEN-LAST:event_jButton2ActionPerformed

