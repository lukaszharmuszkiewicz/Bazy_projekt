    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
         SamolotDao sdao = new JdbcSamolotDao();
         Samolot samolot = new Samolot();
       samolot = sdao.findById(Integer.parseInt(this.jTextField7.getText()));
       this.jTextField1.setText(Integer.toString(samolot.getId_linii_lotniczej()));
       this.jTextField2.setText(samolot.getProducent());
       this.jTextField3.setText(samolot.getModel());
       this.jTextField4.setText(Integer.toString(samolot.getIlosc_miejsc()));
      Format formatter = new SimpleDateFormat("yyyy");
String s = formatter.format(samolot.getRok_produkcji());
       this.jTextField5.setText(s);
       this.jTextField6.setText(samolot.getRodzaj());
            
    }//GEN-LAST:event_jButton5ActionPerformed

