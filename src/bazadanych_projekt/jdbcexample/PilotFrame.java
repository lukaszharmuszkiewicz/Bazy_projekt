    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        PilotDao pdao = new JdbcPilotDao(); 
        Pilot pilot = new Pilot();
       pilot = pdao.findById(this.jTextField6.getText());
       this.jTextField1.setText(pilot.getImie());
       this.jTextField2.setText(pilot.getNazwisko());
       this.jTextField3.setText(Integer.toString(pilot.getNr_licencji()));
         Format formatter = new SimpleDateFormat("dd/MM/yyyy");
String s = formatter.format(pilot.getData_urodzenia());
       this.jTextField4.setText(s);
       this.jTextField5.setText(pilot.getNarodowosc());
    }//GEN-LAST:event_jButton6ActionPerformed

