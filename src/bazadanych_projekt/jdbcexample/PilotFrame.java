    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       PilotDao pdao = new JdbcPilotDao(); 
        Pilot pilot = new Pilot();
        pilot.setImie(this.jTextField1.getText());
        pilot.setNazwisko(this.jTextField2.getText());
        pilot.setNr_licencji(Integer.parseInt(this.jTextField3.getText()));
        pilot.setData_urodzenia(Date.valueOf(this.jTextField4.getText()));
        pilot.setNarodowosc(this.jTextField5.getText());
        pilot.setPesel(this.jTextField6.getText());
        pdao.save(pilot);
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
         PilotDao pdao = new JdbcPilotDao(); 
        List<Pilot> list = pdao.findAll();
       DefaultListModel listModel = new DefaultListModel();
       for(Pilot x : list)
       {
           listModel.addElement(x.toString());
       }
       this.jList1.setModel(listModel);
    }//GEN-LAST:event_jButton4ActionPerformed

