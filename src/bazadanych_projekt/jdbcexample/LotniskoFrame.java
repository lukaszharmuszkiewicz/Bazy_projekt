    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
      LotniskoDao pdao = new JdbcLotniskoDao(); 
        Lotnisko lot = new Lotnisko();
        lot.setId_lotniska(this.jTextField1.getText());
lot.setMiasto(this.jTextField2.getText());
lot.setPanstwo(this.jTextField3.getText());
lot.setLiczba_terminali(Integer.parseInt(this.jTextField4.getText()));
lot.setStrefa_czasowa(this.jTextField5.getText());

pdao.save(lot);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
    LotniskoDao pdao = new JdbcLotniskoDao(); 
        List<Lotnisko> list = pdao.findAll();
       DefaultListModel listModel = new DefaultListModel();
       for(Lotnisko x : list)
       {
           listModel.addElement(x.toString());
       }
       this.jList1.setModel(listModel);
    }//GEN-LAST:event_jButton4ActionPerformed

